<h1>Apa Itu laravel ? </h1>
<div>
<br>Laravel adalah framework untuk membuat aplikasi website dengan basis bahasa pemrograman PHP. 
<br>Laravel memungkinkan Anda untuk bisa membuat suatu aplikasi website secara custom. 
<br>Laravel bersifat open source, jadi bisa digunakan sepenuhnya secara gratis.
<br>Laravel sendiri dibuat oleh Taylor Otwell. Taylor Otwell berusaha menciptakan framework
<br>alternatif dengan fitur yang lebih mumpuni. Pada akhirnya, pada tahun 2011 Laravel berhasil dirilis.
</div>

